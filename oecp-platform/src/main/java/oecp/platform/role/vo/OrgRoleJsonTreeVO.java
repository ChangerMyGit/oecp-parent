package oecp.platform.role.vo;

import oecp.framework.web.ext.JsonTreeVO;

/**
 * 带选择属性的树VO 添加组织角色Id属性
 * 
 * @author wangliang
 * @date 2011-5-27
 */
public class OrgRoleJsonTreeVO extends JsonTreeVO {
	private static final long serialVersionUID = 1L;
	// 选择属性
	private Boolean checked;
	// 组织角色主键
	private String orgRoleId;

	public Boolean getChecked() {
		return checked;
	}

	public void setChecked(Boolean checked) {
		this.checked = checked;
	}

	public String getOrgRoleId() {
		return orgRoleId;
	}

	public void setOrgRoleId(String orgRoleId) {
		this.orgRoleId = orgRoleId;
	}

}
